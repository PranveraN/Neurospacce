// ─── Scoring ──────────────────────────────────────────────────────
export function scorePersonaMatrix(answers) {
  // answers: { questionId: 1-5 }
  const catMap = {}
  PM_CATEGORIES.forEach((c) => { catMap[c.id] = [] })

  PM_QUESTIONS.forEach((q) => {
    const val = answers[q.id]
    if (val !== undefined) catMap[q.cat].push(val)
  })

  const avg = (arr) => arr.length ? arr.reduce((a, b) => a + b, 0) / arr.length : 3
  const scale = (x) => Math.round(((x - 1) / 4) * 100)

  const rawSocial     = avg([...catMap.social, ...catMap.communication])
  const rawEmotional  = avg([...catMap.emotional, ...catMap.stress])
  const rawAnalytical = avg([...catMap.decision, ...catMap.discipline])
  const rawDrive      = avg([...catMap.leadership, ...catMap.ambition])
  const rawCreativity = avg(catMap.creativity)
  const rawAdapt      = avg(catMap.adaptability)

  const scores = {
    social:       scale(rawSocial),
    emotional:    scale(rawEmotional),
    analytical:   scale(rawAnalytical),
    drive:        scale(rawDrive),
    creativity:   scale(rawCreativity),
    adaptability: scale(rawAdapt),
  }

  const code = [
    scores.social     >= 50 ? 'E' : 'I',
    scores.emotional  >= 50 ? 'S' : 'R',
    scores.analytical >= 50 ? 'A' : 'N',
    scores.drive      >= 50 ? 'B' : 'G',
  ].join('')

  const archetype = PM_ARCHETYPES.find((a) => a.code === code) || PM_ARCHETYPES[0]

  const metrics = {
    emotionalIntelligence: Math.round(scores.social * 0.4 + scores.adaptability * 0.3 + scores.emotional * 0.3),
    socialEnergy:          scores.social,
    resilience:            Math.round(Math.max(0, 100 - scores.emotional)),
    strategicThinking:     Math.round(scores.analytical * 0.6 + scores.drive * 0.4),
    creativityIndex:       scores.creativity,
    leadershipIndex:       Math.round(scores.drive * 0.5 + scores.social * 0.3 + scores.analytical * 0.2),
    adaptabilityIndex:     scores.adaptability,
  }

  return {
    testId:    'personaMatrix',
    code,
    archetype,
    scores,
    metrics,
    profile:   archetype,
    completedAt: new Date().toISOString(),
  }
}

